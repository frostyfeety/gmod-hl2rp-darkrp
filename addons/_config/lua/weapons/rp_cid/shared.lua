function SWEP:Initialize()
	self:SetWeaponHoldType( "pistol" )
	self.LastUse = CurTime()

	if CLIENT then
	

		self.VElements = table.FullCopy( self.VElements )
		self.WElements = table.FullCopy( self.WElements )
		self.ViewModelBoneMods = table.FullCopy( self.ViewModelBoneMods )

		self:CreateModels(self.VElements) 
		self:CreateModels(self.WElements) 

		if IsValid(self.Owner) then
			local vm = self.Owner:GetViewModel()
			if IsValid(vm) then
				self:ResetBonePositions(vm)
				
				if (self.ShowViewModel == nil or self.ShowViewModel) then
					vm:SetColor(Color(255,255,255,255))
				else
					vm:SetColor(Color(255,255,255,1))
					vm:SetMaterial("Debug/hsv")			
				end
			end
		end
		
	end

end

function SWEP:PrimaryAttack()
	self:SetWeaponHoldType( "pistol" ) 
	if CLIENT then return end
	if self.LastUse > CurTime() then return end

	local owner = self:GetOwner()
	local trace = owner:GetEyeTrace().Entity

	if not trace:IsPlayer() then return end
	if trace:GetPos():DistToSqr(owner:GetPos()) > 16384 then return end

	self.LastUse = CurTime() + 5

	DarkRP.notify(owner, 0, 5, "Вы пытаетесь показать свою CID-карту " .. trace:Nick())

	local function show_card(answer)
		if not tobool(answer) then return end

		net.Start("rp_viewcard")
		    net.WriteString(owner:Nick())
		    net.WriteString(owner:GetRP_ID())
            net.WriteString(owner:LoyalNumber())
		net.Send(trace)

		DarkRP.notify(owner, 0, 4, "Вы показали " .. trace:Nick() .. " CID-карту")
	end

	DarkRP.createQuestion(owner:Nick() .. " хочет показать вам свою CID карту", "rp_showcard" .. tostring(owner:EntIndex()), trace, 8, show_card)
end

